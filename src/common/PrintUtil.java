package common;

public class PrintUtil {
    public static void printIntArray(int[] array){
        for (int j : array) {
            System.out.print(j + " ");
        }
        System.out.println();
    }

    public static void printCharArray(char[] array){
        for (int j : array) {
            System.out.print(j + " ");
        }
        System.out.println();
    }
}
